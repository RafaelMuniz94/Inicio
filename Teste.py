class Pessoa:
    def __init__(self,idade,nome,peso):
        self.idade = idade
        self.nome = nome
        self.peso = peso

    def retorna_pessoa(self):
        return '(Nome: {},\nIdade: {}, \nPeso: {})'.format(self.nome,self.idade,self.peso)


pessoa = Pessoa(12, 'Rafael', 90)
print(type(pessoa))

print(pessoa.peso, pessoa.nome, pessoa.idade)

print(pessoa.retorna_pessoa())



